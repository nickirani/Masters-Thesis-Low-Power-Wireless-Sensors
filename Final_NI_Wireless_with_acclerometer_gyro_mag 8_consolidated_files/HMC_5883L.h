#ifndef HMC_5883L_H_
#define HMC_5883L_H_

// Configuration Registers
#define CRA   0x00        // Configuration Register A
#define CRB   0x01        // Configuration Register B
#define MR    0x02        // Mode Register
#define DXRA  0x03        // Data Output X MSB Register
#define DXRB  0x04        // Data Output X LSB Register
#define DYRA  0x05        // Data Output Z MSB Register
#define DYRB  0x06        // Data Output Z LSB Register
#define DZRA  0x07        // Data Output Y MSB Register
#define DZRB  0x08        // Data Output Y LSB Register
#define SR    0x09        // Status Register
#define IRA   0x0A        // Identification Register A
#define IRB   0x0B        // Identification Register B
#define IRC   0x0C        // Identification Register C





#endif /* HMC_5883L_H_ */
